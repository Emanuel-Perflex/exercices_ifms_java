package poo.heranca;

public abstract class Pessoa {
    private String nome;
    private String endereco;

    public nome getNome() {
        return this.Nome;
    }

    public endereco getEndereco() {
        return this.Endereco;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

}
